function js_style (){
    let changeStyle = document.getElementById('text') //I targetted the id:text, and assigned it to the variable changeStyle
    changeStyle.style.color = 'blue' //I added color blue to the changeStyle variable by using the style element, it changed the color to blue
    changeStyle.style.fontSize = '40px' //changed the font size to 30px
    changeStyle.style.fontFamily = 'vedana' //changed the font family
}

//the output is, when i click on the style button, the text changes to blue, and an increased font